#!/bin/bash

# export user defined variables
source ./kind_vars.sh

deleteCluster() {
  echo >&2 '
            ************************************************************************
            ********************** DELETING CLUSTER ********************************
            ************************************************************************
            '

  kind delete cluster --name "$KIND_CLUSTER_NAME"

  echo >&2 '
            ************************************************************************
            ********************** CLUSTER DELETED *********************************
            ************************************************************************
            '
}

deleteCluster
